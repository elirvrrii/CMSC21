#include <stdio.h>

int main(void)
{
    
// do-while
 int i;
 i = 1;
 do{
     printf(" %d", i);
     i += 1;
 } 
 while(i < 10);


//for loop
 int j;
 j = 1;
 for(; j< 10;){
     printf(" %d", j);
     j += 1;
 }
 
// while loop
 int k;
 k = 1;
 while(k < 10){
     printf(" %d", k);
     k += 1;
 }
 return 0;
}
 